					<h3>МИКРОФЛОРА КИШЕЧНИКА</h3>
					<p>В последние годы отмечается повсеместное возрождение интереса к кишечной микрофлоре (МИКРОБИОТА КИШЕЧНИКА) и ее влиянию на здоровье и болезни человека. Появились новые факты, свидетельствующие о связи кишечного биоценоза с заболеваниями не только желудочно – кишечного тракта (ЖКТ), но и сердечно-сосудистой системы, ожирением, сахарным диабетом, злокачественными новообразованиями, аллергическими и аутоиммунными болезнями. В целом, изучение влияния микрофлоры ЖКТ на здоровье человека и его болезни имеет давнюю историю.
</p>
<p>Впервые гипотезу о совместном существовании различных видов бактерий в желудочно-кишечном тракте выдвинул в 1681 г. Антони Ван Левенгук после обнаружения микроорганизмов в фекалиях человека. В 1850 г. Луи Пастер развил концепцию о функциональной роли бактерий в ферментационном процессе. Однако основоположником учения о симбионтной микрофлоре и ее влиянии на организм человека по праву считается великий русский ученый, лауреат Нобелевской премии за 1908 год Илья Ильич Мечников. Еще в 1888 г., работая в Институте Луи Пастера, И.И. Мечников высказал предположение, что причиной возникновения многих болезней является совокупное действие на клетки и ткани макроорганизма разнообразных токсинов и других метаболитов, продуцируемых бактериями, во множестве обитающими в пищеварительном тракте и оказывающими на организм «аутоинтоксикационный эффект». В своих работах «Этюды оптимизма» и «Этюды о природе человека» И.И. Мечников писал о том, что многочисленные ассоциации микробов, населяющих кишечник человека, в значительной мере определяют его духовное и физическое здоровье. Также И.И. Мечников полагал, что введение в ЖКТ «здравословных» бактерий способно модифицировать действие кишечной микрофлоры и противодействовать интоксикации. Практическим воплощением идей И.И. Мечникова стало применение ацидофильных лактобацилл с терапевтическими целями, которое было начато в США в 1920–1922 годах.
</p>
<p><span>МИКРОФЛОРА ЧЕЛОВЕКА</span> очень разнообразна – соприкасающийся с внешней средой организм человека населен по разным данным от 500 до 5000 видов бактерий, не считая персистирующие вирусы, простейшие и грибы. 
</p>
<p><span>МИКРОФЛОРА КИШЕЧНИКА ПОДРАЗДЕЛЯЕТСЯ</span> на главную или облигатную (обязательная), факультативную (сапрофитная и условно-патогенная) и транзиторную (случайная). 
</p>
<p>В состав <span>ОБЛИГАТНОЙ МИКРОФЛОРЫ</span> входят анаэробы (бифидобактерии, пропионобактерии, пептострептококки) и аэробы (лактобактерии, энтерококки и эшерихии). 
</p>
<p><span>ФАКУЛЬТАТИВНАЯ МИКРОФЛОРА</span> представлена сапрофитами (бактериоиды, пептококки, стафилококки, стрептококки, бациллы, дрожжевые грибы) и аэробными и анаэробными
бациллами. 
</p>
<p><span>К УСЛОВНО-ПАТОГЕННЫМ ЭНТЕРОБАКТЕРИЯМ</span> относятся представители семейства кишечных бактерий: клебсиеллы, протеи, цитробактеры, энтеробактеры и др.
</p>
<p><span>НОРМАЛЬНАЯ КИШЕЧНАЯ МИКРОФЛОРА ВЫПОЛНЯЕТ РЯД ВАЖНЫХ ФУНКЦИЙ В ОРГАНИЗМЕ ЧЕЛОВЕКА.</span><br/>
Ведущая роль нормальных кишечных бактерий сводится к защите организма от колонизации условно-патогенными и патогенными бактериями и предупреждению избыточного бактериального роста в кишке за счет выработки определенных ингибирующих рост патогенов субстанций. Такое бактериостатическое действие оказывают низкомолекулярные метаболиты сахаролитической микрофлоры – молочная кислота и короткоцепочечные жирные кислоты (пропионовая, масляная, валериановая, уксусная и др.). 
</p>
<p>Кишечная микрофлора способна синтезировать витамины, в частности, витамины группы В, витамины К и С, никотиновую и фолиевую кислоты. Кроме того, молочная и уксусная кислоты, продуцируемые бактериями в результате метаболизма, способствуют усилению процессов всасывания в стенке кишечника ионов кальция, железа, витамина Д.
</p>
<p>Бактерии, населяющие толстый кишечник, участвуют в реализации иммунологических защитных механизмов. Так, известно, что они стимулируют лимфоидный аппарат кишечника, препятствуют деградации секреторного IgA, стимулируют образование интерферона, образование иммуноглобулинов и иммунокомпетентных клеток и способствуют активации иммунных реакций, создавая иммунологическую резистентность. 
</p><p>
Являясь одной из мощнейших естественных защитных систем организма, нормальная микробиота кишечника также выполняет ряд других, не менее важных функций, таких как трофическая и энергетическая стимуляции перистальтики кишечника, участие в дифференцировке и регенерации тканей, детоксикация и выведение эндо- и экзогенных ядовитых соединений, разрушение мутагенов, активация действия лекарственных веществ, поддержание ионного гомеостаза, образование сигнальных молекул (нейротрансмиттеров) и другое.
</p>				</div>
