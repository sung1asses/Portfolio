				<h3>АНТИБИОТИКИ</h3>
					<p>В настоящее время при сверхактивном использовании в лечении антибиотиков одной из важнейших проблем современной медицины является развитие антибиотико-ассоциированной диареи.                                                                                                                                                                                                                                                                                                                                           
</p><p>Под антибиотико-ассоциированной диарей (ААД) следует подразумевать появление не менее трех эпизодов неоформленного стула в течение двух или более последовательных дней на фоне антибиотикотерапии. Признаки ААД (послабленный стул) появляются как во время лечения антибиотиками, так и вплоть до четырехнедельного срока после их отмены.
</p><p>По данным разных авторов ААД преимущественно развивается у принимающих антибиотики лиц пожилого возраста (особенно у женщин), детей, больных с сопутствующей патологией (болезни органов пищеварения, почек, сахарный диабет, злокачественные новообразования, врожденный или приобретенный иммунодефицит и др.). Степень тяжести ААД колеблется от субклинического, доброкачественного течения до тяжелого, проявляющегося в виде псевдомембранозного колита (ПМК). 
</p><p>По механизму развития, клиническим признакам и тяжести процесса можно выделить две основные формы ААД: идиопатическая и ПМК.
</p><p>Патогенез идиопатической ААД:<br/>                                                                                                                                                                                                               
</p><p>Гиперкинетическая диарея развивается при применении антибиотиков, содержащих клавулановую кислоту (способствует усилению перистальтики), а также макролидов (раздражают мотилиновые рецепторы, что, в свою очередь, приводит к сокращению антрального отдела желудка и двенадцатиперстной кишки).                                                                                                                                                                                                     
</p><p>Гиперосмолярная диарея наблюдается в результате неполного всасывания цефалоспоринов, что способствует развитию синдрома мальабсорбции.                                                                                                                                                                                                   
</p><p>Токсическая диарея возникает в ответ на прямое токсическое действие тетрациклинов на слизистую желудочно-кишечного тракта. 
</p><p>Секреторная диарея развивается в результате усиления секреции в толстом кишечнике вследствие нарушения деконъюгации желчных кислот за счет повышенного выделения хлоридов и воды. 
</p><p>Дисбиотические процессы, которые возникают вследствие приема антибиотиков, ведущие к изменению количественного и качественного состава нормальной кишечной микрофлоры.
</p><p>Псевдомембранозний колит – более тяжелая форма ААД, ассоциированная с анаэробной грамположительной спорообразующей бактерией Clostridium difficile (Cl. Difficile), т.е. «трудной». Возбудитель не погибает под воздействием антисептиков, содержащих спирт, и подвергается уничтожению хлоргексидином. Споры при низких значениях рН выживают, а вегетирующие формы Cl. difficile погибают при рН=1,0-3,5 желудочного сока.
</p><p>Бессимптомное носительство Cl. difficile составляет около 3-5% среди взрослого населения, однако при антибиотикотерапии (пенициллины, цефалоспорины, клиндамицин, макролиды) частота носительства увеличивается до 15-40%. При приеме антибиотиков происходит угнетение нормальной микрофлоры кишечника и наблюдается колонизация токсигенными клостридиями. Clostridium difficile вырабатывает энтеротоксин А, который повреждает эпителиальные клетки кишечника и способствует развитию непосредственно диареи, и цитотоксины В, оказывающие системное альтеративное действие.
</p><p>Клиническая симптоматика ПМК появляется на 4-9 день после начала приема антибактериального препарата. Начало развития заболевания характеризуется появлением признаков абдоминального дискомфорта, метеоризма, болей в животе с развитием обильной водянистой диареи от 5-7 раз в сутки при легкой степени, до 10-15 раз при среднетяжелой степени и до 30 раз при тяжелой степени заболевания. Сопровождается лихорадкой до 38ºС при среднетяжелой и до 39-40ºС при тяжелой степени на фоне лейкоцитоза и общей интоксикации организма. При выраженной экссудации и значительной потери белка с калом развивается гипоальбуминемия и отеки, иногда отмечается развитие реактивного полиартрита с вовлечением крупных суставов. Возможны серьезные осложнения ПМК в виде обезвоживания, кишечного кровотечения, перитонита, сепсиса. Редко встречаются молниеносные формы ПМК, напоминающие холеру, летальность при отсутствии лечения которых составляет 15-30%.
</p><p>Лечение ААД является комплексным и включает восстановление нормального биоценоза кишечника; специфическую антибактериальную терапию и профилактику рецидивов. По мнению многих авторов, эффективность лечения и профилактики ААД зависит от своевременного назначения пробиотиков, в состав которых входят представители кишечной микрофлоры, помогающие эффективно восстанавливать естественную активность кишечной микрофлоры.
		
