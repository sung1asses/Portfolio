				<h3>ДИСБИОЗ</h3>
					<div class="dis-text">Качественное и/или количественное изменение состава кишечной микрофлоры называют дисбиозом кишечника (ДК).</div>
<div class="dis-text">Частой причиной развития ДК являются заболевания органов пищеварения, особенно сопровождающиеся нарушениями переваривания и кишечного всасывания, при которых изменение состава внутренней среды в кишке меняет условия существования микроорганизмов. 
</div><div class="dis-text">Таким образом, дисбиоз кишечника часто является патогенетическим «ложем» для формирования заболеваний внутренних органов, то есть взаимоотношения между дисбиозом кишечника человека и его внутренними болезнями носят взаимопотенцирующий характер.
