				<h3>АНТИБИОТИКТЕР</h3>
					<p>Қазіргі уақытта емдеуде антибиотиктерді асыра белсенді пайдалану кезінде заманауи медицинаның маңызды проблемаларының бірі антибиотиктік-қауымдасқан диареяның дамуы болып табылады.
</p><p><span>АНТИБИОТИКТІК-ҚАУЫМДАСҚАН ДИАРЕЯ (АҚД)</span> ретінде антибиотикотерапия аясында екі немесе одан да көп бірізді күндер ішінде рәсімделмеген дәреттің кемінде үш эпизодының пайда болуы меңзеледі. АҚД белгілері (жұмсақ дәрет) антибиотиктермен емдеу уақытында, сол сияқты оларды тоқтатқаннан кейін тіпті төрт апталық мерзімге дейін пайда болады.</p>
<p>Әртүрлі авторлардың деректері бойынша, АҚД басымдылықты түрде антибиотиктерді қабылдайтын қарттарда (әсіресе әйелдерде), балаларда, ілеспелі патологиясы бар (ас қорыту органдарының, бүйректерінің ауруы, қант диабеті, қатерлі өскіндер, тума немесе жүре пайда болған иммун тапшылығы және басқасы) науқастарда дамиды. АҚД ауырлық дәрежесі субклиникалық, қатерсіз ағудан жалған мембраналық шаншу (ЖМШ) түрінде пайда болатын ауырға дейін ауытқиды. </p>
<p><span>Даму механизмі</span>, клиникалық белгілері және процесс ауырлығы бойынша АҚД екі негізгі түрлерін бөлуге болады: идиопатиялық және ЖМШ.</p>

<p><span>ИДИОПАТИЯЛЫҚ АҚД ПАТОГЕНЕЗІ:</span><br/>                                                                                                                                                                                                               
</p><p><span>Гиперкинетикалық диарея</span> клавулан қышқылын (перистальтиканың күшеюіне жәрдемдеседі), сонымен қатар макролидтерді (мотилинді рецепторларды тітіркендіреді, бұл өз кезегінде асқазанның және жіңішке ішектің антрал бөлімінің қысқаруына әкеп соқтырады) қамтитын антибиотиктерді қолдану кезінде дамиды.</p>
<p><span>Гиперосмолярлық диарея</span> цефалоспориндердің толық емес сіңірілуі нәтижесінде байқалады, бұл мальабсорбция синдромының дамуына себептес болады.</p>
<p><span>Токсикалық диарея</span> тетрациклиндердің асқазан-ішек жолының шырыштысына тікелей токсикалық әсеріне жауабы ретінде туындайды.</p>
<p><span>Секреторлық диарея</span> хлоридтер мен судың асыра бөлінуі есебінен өрт қышқылдарының деконъюгациясының бұзылуы салдарынан тоқ ішектегі секрецияның күшеюі нәтижесінде дамиды.</p>
<p>Антибиотиктерді қабылдау салдарынан туындайтын, қалыпты ішек микрофлорасының сандық және сапалық құрамының өзгеруіне жетелейтін дисбиотикалық процестер.</p>	
<p><span>Жалған мембраналық шаншу</span> - Clostridium difﬁcile (Cl. Difﬁcile) анаэробты грамоң спора түзуші, яғни «қиын» бактерияға ұқсас АҚД анағұрлым ауыр түрі. Қоздырушы спиртті қамтитын антисептиктердің әсерімен өлмейді және хлоргексидинмен жойылуға ұшыратылады. Спорлар рН төмен мәндері кезінде тірі қалады, ал Cl. difﬁcile вегетациялаушы түрлері асқазан сөлінің рН=1,0-3,5 кезінде өледі. </p>	
<p><span>Cl. difﬁcile симптомсыз тасымалдануы</span> ересек халық арасында шамамен 3-5% құрайды, алайда антибиотикотерапия кезінде (пенициллиндер, цефалоспориндер, клиндамицин, макролидтер) тасымалдану жиілігі 15-40%-ға дейін ұлғаяды. Антибиотиктерді қабылдау кезінде ішектің қалыпты микрофлорасының ауырлауы орын алады және токсигенді клостридийлермен отарлануы байқалады. Clostridium difﬁcile ішектің эпителиалық жасушаларын зақымдайтын және тікелей диареяның және жүйелі баламалы әсерін тигізетін В цитотоксиндердің дамуына көмектесетін А энтеротоксинін шығарады. </p>
<p><span>ЖМШ клиникалық симптоматикасы</span> антибактериялық препаратты қабылдауды бастаудан кейін 4-9 күнде пайда болады. Аурудың дамуының басталуы абдоминальді ыңғайсыздық, метеоризм, жеңіл дәрежесінде тәулігіне 5-7 рет, орташа ауырдәрежесінде 10-15 рет және аурудың ауыр дәрежесі кезінде 30 ретке дейін мол сулы диареяның дамуымен іштегі ауыру белгілерінің пайда болуымен сипатталады. Ағзаның лейкоцитозы және жалпы интоксикациясы аясында орташа ауыр кезінде 38ºС-қа дейін және ауыр дәрежесі кезінде 39-40ºС-қа дейінгі қызу көтерілумен сүйемелденеді. Анық көрінетін экссулациясы және нәжіспен ақуыздың елеулі жоғалуы кезінде гипоальбуминемия және ісіктері дамиды, кейде ірі буындардың жұмылдырылуымен реактивті полиартриттің дамуы байқалады. Сусыздану, ішектің қансырауы, перитонит, сепсис түріндегі ЖМШ елеулі асқынуы болуы мүмкін. Емдеудің жоқтығы кезіндегі өлу оқиғасы 15-30% құрайтын, тырысқаққа ұқсас ЖМШ қауырт түрлері сирек кездеседі.
 </p>
 <p><span>АҚД емдеу</span> кешенді болып табылады және ішектің қалыпты биоценозын қалпына келтіруді; ерекше актибактериялық терапиясын және қайталанудың алдын алуды қамтиды. Көптеген авторлардың пікірі бойынша, АҚД емдеу және алдын алу тиімділігі пробиотиктерді уақтылы тағайындаудан тәуелді, олардың құрамына ішек микрофлорасының табиғи белсенділігін тиімді қалпына келтіруге көмектесетін ішек микрофлорасының өкілдері кіреді.</p>
