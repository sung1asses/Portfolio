					<h3>ІШЕК МИКРОФЛОРАСЫ</h3>
					<p>Соңғы жылдары ішек микрофлорасына (шетелде микробиота термині қабылданды) қызығушылықтың жаппай жандануы және оның адам денсаулығы мен ауруларына әсері байқалады. Ішек биоценозының тек асқазан-ішек жолдарының (АІЖ) ауруларымен ғана емес, сонымен қатар жүрек-қантамыр жүйесінің, семіздіктің, қант диабетінің, қатерлі ісіктердің, аллергиялық және аутоиммундық аурулармен байланысын куәландыратын жаңа фактілер пайда болды. Жалпы, АІЖ микрофлорасының адам денсаулығына және оның ауруына әсерін зерттеудің ұзақ тарихы бар.</p>
<p>Алғаш рет асқазан-ішек жолында бактериялардың әртүрлі түрлерінің бірлескен болуы туралы гипотезаны 1681 ж. Антони Ван Левенгук адамның нәжістерінде микроорганизмдер анықтағаннан кейін ұсынды. 1850 жылы Луи Пастер ферментациялық үдерістегі бактериялардың функционалдық рөлі туралы концепцияны дамытады. Бірақ симбионтты микрофлора және оның адам ағзасына әсері туралы ілімнің негізін қалаушы ұлы орыс ғалымы, 1908 жылғы Нобель сыйлығының лауреаты Илья Ильич Мечников. 1888 жылы Луи Пастер институтында жұмыс істей отырып, И.И. Мечников көптеген аурулардың пайда болуының себебі ас қорыту жолында мекендейтін және ағзаға «аутоинтоксикациялық әсер» беретін бактериялар өндіретін әртүрлі токсиндер мен басқа да метаболиттердің макроорганизмінің жасушалары мен тіндеріне жиынтық әсер ету болып табылады деген болжам айтты. Өзінің «Оптимизм этюдтары» және «Адам табиғаты туралы этюдтар» еңбектерінде И.И. Мечников адамның ішегін мекендейтін микробтардың көптеген ассоциациялары оның рухани және физикалық денсаулығын едәуір анықтайды деп жазды. Сондай-ақ, И.И. Мечников АІЖ-ға «сау» бактерияларын енгізу ішек микрофлорасының әсерін түрлендіруге және улануға қарсы әрекет етуге қабілетті деп ойлады. И.И. Мечниковтың идеяларын іс жүзінде іске асыру 1920-1922 жылдары АҚШ-та басталған терапевтік мақсаттармен ацидофильді лактобациллдерді қолдану болды.</p>
<p><span>АДАМ МИКРОФЛОРАСЫ</span> өте әртүрлі – сыртқы ортамен жанасатын адам ағзасына әртүрлі деректер бойынша ұзақ сақталушы вирустарды, қарапайым және саңырауқұлақтарды санамағанда, 500-ден 5000-ға дейінгі бактериялардың түрлері орналасқан. </p>
<p><span>ІШЕКТІҢ МИКРОФЛОРАСЫ</span> басты немесе облигатты (міндетті), факультативтік (сапрофитті және шартты-патогенді) және транзиторлық (кездейсоқ) болып бөлінеді. </p>
<p><span>ОБЛИГАТТЫ МИКРОФЛОРАНЫҢ</span> құрамына анаэробтар (бифидобактериялар, пропионобактериялар, пептострептококтар) және аэробтар (лактобактериялар, энтерококтар және эшерихиялар) кіреді.</p>
<p><span>ФАКУЛЬТАТИВТІ МИКРОФЛОРА</span> сапрофиттермен (бактероидтар, пептокококтар, стафилокококтар, стрептокококтар, бациллалар, ашытқы саңырауқұлақтары) және аэробты және анаэробты бациллалармен ұсынылған. </p>
<p><span>ШАРТТЫ-ПАТОГЕНДІ ЭНТЕРОБАКТЕРИЯЛАРҒА</span> ішек бактериялары тобының өкілдері жатады: клебсиелалар, протеиндер, цитробактерлер, энтеробактерлер және т. б.</p>
<p><span>ҚАЛЫПТЫ ІШЕК МИКРОФЛОРАСЫ АДАМ АҒЗАСЫНДА БІРҚАТАР МАҢЫЗДЫ ФУНКЦИЯЛАРДЫ ОРЫНДАЙДЫ.</span><br/> Қалыпты ішек бактерияларының жетекші рөлі ағзаны шартты-патогенді және патогенді бактериялармен отарлаудан қорғауға және субстанциялардың патогендерінің белгілі бір өсуін тежейтін шығару есебінен ішекте артық бактериялық өсудің алдын алуға әкеледі. Мұндай бактериостатикалық әсер сахаролитикалық микрофлораның төмен молекулалық метаболиттері – сүт қышқылы және қысқа тізбекті май қышқылдары (пропиондық, майлы, валериан, сірке суы және т. б.) көрсетеді. </p>
<p>Ішек микрофлорасы витаминдерді, атап айтқанда В тобының витаминдерін, К және С витаминдерін, никотин және фолий қышқылын синтездеуге қабілетті. Бұдан басқа, метаболизм нәтижесінде бактериялар өндіретін сүт және сірке қышқылдары ішек қабырғасында кальций, темір, Д витамині иондарының сіңу үдерістерінің күшеюіне ықпал етеді.</p>
<p>Тоқ ішекті мекендейтін бактериялар иммунологиялық қорғаныш механизмдерін іске асыруға қатысады. Сонымен, олар ішектің лимфоидты аппаратын ынталандырады, секреторлы igА тоқырауына кедергі жасайды, интерферонның түзілуін, иммуноглобулиндер мен иммунды құзыретті жасушалардың түзілуін ынталандырады және иммунологиялық резистенттілікті жасай отырып, иммундық реакциялардың белсендірілуіне ықпал етеді.</p>
<p>Ағзаның ең күшті табиғи қорғаныш жүйелерінің бірі бола отырып, ішектің қалыпты микробиотасы, сондай-ақ ішек перистальтикасын трофикалық және энергетикалық ынталандыру, ұлпаларды саралауға және регенерациялауға қатысу, эндо-және экзогенді улы қосылыстарды детоксикациялау және шығару, мутагендердің бұзылуы, дәрілік заттардың әсерінің активациясы, иондық гомеостазды қолдау, сигналдық молекулалардың (нейротрансмиттерлердің) түзілуі және басқа да маңызды функцияларды орындайды.</p>				</div>
